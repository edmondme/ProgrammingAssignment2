# Return the inverse of matrix. Check the inverse is already set, if it is, then get the result
# and skip the rest. If not, compute the inverse, set the value in cache by calling the setinverse.

cacheSolve <- function(x, ...) {
    inv <- x$getinverse()
    if(!is.null(inv)) {
        message("getting cached data.")
        return(inv)
    }
    data <- x$get()
    inv <- solve(data)
    x$setinverse(inv)
    inv
}
